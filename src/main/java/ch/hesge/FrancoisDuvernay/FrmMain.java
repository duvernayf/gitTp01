    private void lstCadeauxItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_lstCadeauxItemStateChanged
        if (lstCadeaux.getSelectedIndex() == -1) {
            btnAjouter.setEnabled(false);
        } else {
            Cadeau c = (Cadeau)listCadeaux.get(lstCadeaux.getSelectedIndex());
            Personne p = (Personne)listPersonnes.get(lstPersonnes.getSelectedIndex());
            ArrayList pList = p.getIdsCadeaux();
            boolean dedans = false;
            for (int i = 0; i < pList.size(); i++) {
                Cadeau actuel = new Cadeau(Integer.parseInt((String)pList.get(i)));
                if(c.equals(actuel)) {
                    dedans = true;
                }
            }
            if (dedans) {
                btnAjouter.setEnabled(false);
            }
            else{
                btnAjouter.setEnabled(true);
            }
        }
    }//GEN-LAST:event_lstCadeauxItemStateChanged

    private void btnAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAjouterActionPerformed
        Personne p = (Personne) listPersonnes.get(lstPersonnes.getSelectedIndex());
        ArrayList idsCadeau = p.getIdsCadeaux();
        String index = String.valueOf(lstCadeaux.getSelectedIndex() + 1);
        idsCadeau.add(index);
        Personne remplace = new Personne(p.getNom(), idsCadeau);
        listPersonnes.set(lstPersonnes.getSelectedIndex(), remplace);
        initListeCadeauCourante(remplace);
        btnAjouter.setEnabled(false);
    }//GEN-LAST:event_btnAjouterActionPerformed

    /*MÃ©thode pour transformer les tableaux en liste pour se faciliter la tÃ¢che.*/
    private ArrayList tabToList(String[] tab) {
        ArrayList liste = new ArrayList();
        for (int i = 0; i < tab.length; i++) {
            liste.add(tab[i]);
        }
        return liste;
    }//tabToList

    private void initListePers() {
        ArrayList liste = tabToList(FileStr.read(PERSONNES));
        for (int i = 0; i < liste.size(); i++) {
            ArrayList num = new ArrayList();
            StringTokenizer st = new StringTokenizer((String) liste.get(i), DEL_MOTS);
            String nom = st.nextToken();
            lstPersonnes.add(nom);
            while (st.hasMoreTokens()) {
                num.add(st.nextToken());
            }
            Personne p = new Personne(nom, num);
            listPersonnes.add(p);
        }
    }//initListePers

    private void initListeCadeau() {
        ArrayList liste = tabToList(FileStr.read(CADEAUX));
        for (int i = 0; i < liste.size(); i++) {
            StringTokenizer st = new StringTokenizer((String) liste.get(i), DEL_MOTS);
            int id = Integer.parseInt(st.nextToken());
            String libelle = st.nextToken();
            int prix = Integer.parseInt(st.nextToken());
            Cadeau c = new Cadeau(id, libelle, prix);
            lstCadeaux.add(c.toString());
            listCadeaux.add(c);
        }
    }//initListeCadeau

    private void initListeCadeauCourante(Personne p) {
        lstCadeauxCourante.clear();
        lblCadeauxPour.setText(CADEAUX_POUR + p.getNom());
        ArrayList idsCadeaux = p.getIdsCadeaux();
        int somme = 0;
        for (int i = 0; i < idsCadeaux.size(); i++) {
            int index = Integer.parseInt((String) idsCadeaux.get(i)) - 1;
            Cadeau c = (Cadeau) listCadeaux.get(index);
            lstCadeauxCourante.add(c.toString());
            somme += c.getPrix();
        }
        lblSomme.setText(SOMME + somme + ".-");
    }//initListeCadeauCourante

